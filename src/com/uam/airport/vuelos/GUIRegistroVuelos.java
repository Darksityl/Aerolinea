public void duracionVuelo(){
    
    int tmpllegada;
    int tmpSalida;

    
    
    this.llegada = jDateChooserDateArrive.getCalendar();
    this.salida = jDateChooserDateOut.getCalendar();
    this.llegada.set(Calendar.HOUR_OF_DAY, Integer.parseInt(jComboBoxLlegada.getSelectedItem().toString()));
    this.llegada.set(Calendar.MINUTE, Integer.parseInt(jComboBoxMinutosLlegada.getSelectedItem().toString()));
    this.salida.set(Calendar.HOUR_OF_DAY, Integer.parseInt(jComboBoxHoraSalida.getSelectedItem().toString()));
    this.salida.set(Calendar.MINUTE, Integer.parseInt(jComboBoxMinutosSalida.getSelectedItem().toString()));
    
    
    tmpllegada=llegada.get(Calendar.DAY_OF_YEAR)*24*60+llegada.get(Calendar.HOUR_OF_DAY)*60+llegada.get(Calendar.MINUTE);
    tmpSalida=salida.get(Calendar.DAY_OF_YEAR)*24*60+salida.get(Calendar.HOUR_OF_DAY)*60+salida.get(Calendar.MINUTE);
    
    this.dias=(tmpllegada-tmpSalida)/60/24;
    this.horas=(tmpllegada-tmpSalida)/60;
    this.minutos=(tmpllegada-tmpSalida)-this.horas*60;

    duracion = " Horas: "+this.horas+" Minutos: "+this.minutos;
}

  public void mostrar(){
    String matris[][] = new String[this.registroAeronaves.sizeAvion()][6];
    DefaultTableModel tModel = (DefaultTableModel)this.jTable1.getModel();
    
      for (int i = 0; i < registroAeronaves.sizeAvion(); i++) {
          matris[i][0]=registroAeronaves.getAvion(i).getNombre();
          matris[i][1]=registroAeronaves.getAvion(i).getMatricula();
          matris[i][2]=registroAeronaves.getAvion(i).getModelo();
          matris[i][3]=Integer.toString(registroAeronaves.getAvion(i).getCantidadAsientosDisponibles());
          matris[i][4]=Integer.toString(registroAeronaves.getAvion(i).getCantidadAsientosPrimeraClases());
          matris[i][5]=(Boolean.toString(registroAeronaves.getAvion(i).isActiva()));
          
      }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Nombre", "Matricula", "Avion", "Espacios", "Primera Clase", "Activo"
            }
        ));
      
    
} 



public boolean isCorrect(){
        return this.dias>=0&&this.horas>=0&&this.minutos>=0;
}

